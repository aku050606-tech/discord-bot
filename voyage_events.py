def events_for_area(area):
    """そのエリアに出る選択肢イベントの (id, 重み) リスト。"""
    out = []
    for eid, d in EVENT_DEFS.items():
        if area in d.get("areas", []):
            out.append((eid, d.get("weight", {}).get(area, 1)))
    return out
